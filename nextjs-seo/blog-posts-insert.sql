## Common Note-Taking Mistakes to Avoid

### Mistake 1: Transcribing Instead of Processing

**The problem:** Copying word-for-word from lectures or textbooks without understanding.

**The solution:**
- Pause after each main point and summarize in your own words
- Write explanations, not transcriptions
- If you can't paraphrase it, you don't understand it yet

### Mistake 2: Highlighting Everything

**The problem:** Excessive highlighting creates a false sense of productivity without improving retention.

**Research shows:** Students who highlight more than 20% of text perform worse on exams than those who highlight <10%.

**The solution:**
- Limit highlighting to key terms and definitions
- Use different colors for different categories (definitions, examples, formulas)
- Combine highlighting with margin notes explaining WHY it's important

### Mistake 3: Never Reviewing Notes

**The problem:** Taking notes once and never looking at them again.

**The solution:**
- Review notes within 24 hours (when memory is fresh)
- Weekly review sessions for all course notes
- Pre-exam review starting 2-3 weeks before test
- Use spaced repetition scheduling

### Mistake 4: Messy, Disorganized Format

**The problem:** Notes without clear structure are difficult to review and study.

**The solution:**
- Use consistent formatting (same heading system)
- Date every set of notes
- Number pages consecutively
- Use dividers or folders to organize by topic
- Create a table of contents or index

### Mistake 5: Passive Note-Taking

**The problem:** Writing notes without engaging with the material.

**The solution:**
- Ask questions in margins
- Create connections to previous knowledge
- Generate examples
- Predict what comes next
- Challenge ideas and evaluate evidence

