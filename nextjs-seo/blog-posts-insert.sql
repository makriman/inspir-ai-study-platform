## Method 6: The SQ3R Method for Textbook Reading

Reading textbooks differs from note-taking during lectures. The SQ3R method (Survey, Question, Read, Recite, Review) transforms passive reading into active learning.

### The Five Steps of SQ3R

**Step 1: Survey (5 minutes)**
Before reading the chapter:
- Read chapter title and introduction
- Scan all headings and subheadings
- Look at images, graphs, and captions
- Read the chapter summary
- Review end-of-chapter questions

This creates a mental framework for organizing information.

**Step 2: Question (2 minutes per section)**
Turn each heading into a question:
- "Photosynthesis" becomes "What is photosynthesis and how does it work?"
- "Causes of WWI" becomes "What were the main causes of World War I?"

Write these questions in your notes before reading each section.

**Step 3: Read (Active reading)**
Read one section at a time:
- Seek answers to your questions
- Highlight sparingly (only key terms and definitions)
- Write brief margin notes
- Stop and summarize mentally after each paragraph

**Step 4: Recite (After each section)**
Without looking at the text:
- Answer the question you created
- Summarize the section in your own words
- Write a 2-3 sentence summary in your notes
- If you can't recite it, re-read the section

**Step 5: Review (After reading entire chapter)**
- Reread all section summaries
- Answer end-of-chapter questions
- Create a mind map of the chapter
- Test yourself on key questions
- Identify concepts needing more study

### SQ3R Note-Taking Example

**Chapter: The French Revolution**

**Survey notes:**
- Chapter covers 1789-1799
- Three main sections: Causes, The Revolution, Consequences
- Key figures: Louis XVI, Robespierre, Napoleon
- Connects to Enlightenment ideas from Chapter 12

**Section 1: Causes of the French Revolution**

**Question:** What were the main causes of the French Revolution?

**Reading notes:**
- Economic crisis: Government bankruptcy, heavy taxation of Third Estate
- Social inequality: Estates system (clergy, nobles, commoners)
- Enlightenment ideas: Liberty, equality, popular sovereignty
- Weak leadership: Louis XVI indecisive, Marie Antoinette unpopular
- Food shortages: Bad harvests 1788-1789, bread prices soared

**Recite:** The French Revolution stemmed from economic crisis (government debt and unfair taxation), rigid social inequality (Estates system), Enlightenment ideas challenging absolute monarchy, weak royal leadership, and immediate trigger of food shortages in late 1780s.

**Key question for review:** How did Enlightenment philosophy contribute to revolutionary sentiment?

## The Feynman Technique: Explaining to Learn

Nobel Prize-winning physicist Richard Feynman developed a note-taking approach centered on explaining concepts simply, as if teaching a child.

### The Four Steps of the Feynman Technique

**Step 1: Choose a concept and write everything you know**
- Pick one concept to master
- Write out everything you understand about it
- Use plain language, no jargon

**Step 2: Pretend to teach it to a child**
- Explain the concept as if to a 12-year-old
- Use simple words and analogies
- Draw diagrams or examples
- If you struggle to explain simply, you don't understand it yet

**Step 3: Identify gaps and go back to source material**
- Where did your explanation get stuck?
- Which parts felt uncertain?
- Return to textbook, lectures, or resources
- Focus on filling specific knowledge gaps

**Step 4: Simplify and create analogies**
- Remove complicated language
- Create memorable analogies or stories
- Connect to everyday experiences
- Test your explanation on someone else

### Feynman Technique Example: Photosynthesis

**Attempt 1 (Technical):**
"Photosynthesis is the process by which photoautotrophs utilize electromagnetic radiation to convert carbon dioxide and water into glucose via light-dependent and light-independent reactions occurring in chloroplast thylakoids and stroma..."

**Identified gap:** Too technical, using jargon, not explaining HOW or WHY.

**Attempt 2 (Simplified):**
"Photosynthesis is how plants make food from sunlight. Think of it like a solar panel combined with a factory. The 'solar panel' part (light reactions) happens in special compartments called thylakoids. These absorb sunlight and use that energy to charge up molecular batteries (ATP and NADPH). Then the 'factory' part (Calvin cycle) takes CO‚ÇÇ from air and uses those charged batteries to build glucose (sugar) molecules. It's like the plant is building LEGO structures (glucose) from individual LEGO bricks (CO‚ÇÇ), powered by captured sunlight energy."

**Result:** Clear explanation using analogies (solar panel, factory, LEGO) that make the process memorable and understandable.

